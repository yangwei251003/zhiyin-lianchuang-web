-- 12. AI 纸价预测（6 纸种 × 37 天 = 222 行）
--   - 过去 30 天：is_predicted=false（历史值）
--   - 未来 7 天：is_predicted=true（AI 预测值）
-- 价格围绕基准价波动；change_rate 为涨跌幅百分比；ai_analysis 区分历史/预测文案
-- 用 generate_series 生成，ON CONFLICT (paper_type, date) DO NOTHING 去重
-- ----------------------------------------------------------------------------
insert into public.price_predictions (paper_type, date, price, change_rate, ai_analysis, is_predicted, created_at)
select
    pt.paper_type,
    d::date,
    round((pt.base_price + (random() - 0.5) * pt.base_price * 0.08)::numeric, 2),
    round(((random() - 0.5) * 5)::numeric, 2),
    case
        when d::date > current_date then
            'AI 预测：基于近期走势与原材料指数，预计该纸种价格将保持' ||
            case when random() > 0.5 then '小幅上行' else '震荡盘整' end ||
            '，建议关注库存周期。'
        else
            '历史价格数据：当日成交均价参考。'
    end,
    d::date > current_date,
    now()
from (
    values
        ('铜版纸',   6200),
        ('双胶纸',   5500),
        ('白卡纸',   4800),
        ('哑粉铜版纸', 7000),
        ('轻涂纸',   4200),
        ('新闻纸',   3800)
) as pt(paper_type, base_price)
cross join generate_series(
    current_date - interval '30 days',
    current_date + interval '7 days',
    interval '1 day'
) as d
on conflict (paper_type, date) do nothing;

